public class Bitwise {
    public static void main(String[] args){
        int a = 20;
        int b = 10;
        System.out.println("a&b: "+(a&b));
        System.out.println("a|b: "+(a|b));
        System.out.println("a^b: "+(a^b));
        System.out.println("b<<2: "+(b<<2));
        System.out.println("b>>2: "+(b>>2)); 
    }
}